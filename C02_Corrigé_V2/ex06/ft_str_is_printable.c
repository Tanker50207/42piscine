int	ft_str_is_printable(char *str)
{
	int	count;

	count = 0;
	if (str[0] == '\0')
		return (1);
	while (str[count] != '\0')
	{
		if (!(' ' <= str[count] && str[count] <= '~'))
			return (0);
		count++;
	}
	return (1);
}
